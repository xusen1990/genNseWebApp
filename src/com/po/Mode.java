package com.po;

public class Mode {
	
	private String nseMode;
	private boolean enAd;
	private boolean enAcl;
	private boolean enLpm;
	
	public Mode(){}

	public String getNseMode() {
		return nseMode;
	}

	public void setNseMode(String nseMode) {
		this.nseMode = nseMode;
	}

	public boolean isEnAd() {
		return enAd;
	}

	public void setEnAd(boolean enAd) {
		this.enAd = enAd;
	}

	public boolean isEnAcl() {
		return enAcl;
	}

	public void setEnAcl(boolean enAcl) {
		this.enAcl = enAcl;
	}

	public boolean isEnLpm() {
		return enLpm;
	}

	public void setEnLpm(boolean enLpm) {
		this.enLpm = enLpm;
	}
	
	

}
